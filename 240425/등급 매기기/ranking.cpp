#include <iostream>
using namespace std;

int main() {
    int n;
    cin>>n;
    if(n>=90){
        cout<<"A"<<endl;
    }
    else if(n>=80){
        cout<<"B"<<endl;
    }
    else if(n>=70){
        cout<<"C"<<endl;
    }
    else if(n>=60){
        cout<<"D"<<endl;
    }
    else{
        cout<<"F"<<endl;
    }
    // 여기에 코드를 작성해주세요.
    return 0;
}